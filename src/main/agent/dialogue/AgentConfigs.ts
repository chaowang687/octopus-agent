export function updateAgentPromptsWithTools(configs: Map<string, DialogueAgent>): void {
  const toolsDescription = toolRegistry.getToolsDescription()
  
  configs.forEach((config) => {
    const toolsSection = `

**【可用工具列表】**

你有以下工具可以使用：
${toolsDescription}

使用工具的注意事项：
1. 确保工具参数正确，特别是必需参数
2. 使用绝对路径，不要使用相对路径
3. 根据任务选择合适的工具
4. 如果工具执行失败，检查错误信息并重试
`
    config.systemPrompt = config.systemPrompt + toolsSection
  })
}

